package br.com.dogrepo.util


import androidx.lifecycle.LiveData

//to support "empty" Livedata
class AbsentLiveData<T : Any?> private constructor() : LiveData<T>() {
    init {
        postValue(null)
    }

    companion object {
        fun <T> create(): LiveData<T> {
            return AbsentLiveData()
        }
    }
}